#!/usr/bin/env python3
"""
This script is an introduction to variables in Python.
Variables store data values that can be used throughout a program.
In Python, a variable is created when you assign a value to it. 
Python is dynamically typed, so you don't need to declare data types explicitly.
"""

# Example of integer variable assignment
number = 10

# Example of float variable assignment
pi = 3.14159

# Example of string variable assignment
name = "Alice"

# Multiple variable assignment in one line
a, b, c = 1, 2.5, "Hello"

# Output the variable values
print("The value of number is:", number)
print("The value of pi is:", pi)
print("The name is:", name)
print("Multiple variable assignment: a = {}, b = {}, c = {}".format(a, b, c))